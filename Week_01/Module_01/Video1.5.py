number1 = 45
number2 = 5

# Try simple mathematical operators.
print(number1 + number2)
print(number1 - number2)
print(number1 * number2)
print(number1 / number2)

print(10 / 3)       # output = 3.3333333333333335   Quotient with decimal
print(10 // 3)      # output = 3                    Quotient without decimal
print(10 % 3)       # output = 1                    Remainder

