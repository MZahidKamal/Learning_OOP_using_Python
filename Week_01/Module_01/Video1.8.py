# Range based for loop in Python. Starting from 0 and ending before 5.
for i in range(5):
    print(i)

# Range based for loop in Python. Starting from 1 and ending before 10.
for i in range(1, 10):
    print(i)

# Range based for loop in Python. Starting from 1, ending before 10, incrementing 2.
for i in range(1, 10, 2):
    print(i)
